package exceptions;

/**
 * This defines a checked exception for illegal arguments.
 * 
 * @author Natacha Gabbamonte 0932340
 * 
 */
public class DAIllegalArgumentException extends Exception {

	private static final long serialVersionUID = 3L;

	/**
	 * Constructor with only an error message.
	 * 
	 * @param message
	 *            The error message
	 */
	public DAIllegalArgumentException(String message) {
		super(message);
	}

	/**
	 * Constructor with only a throwable cause.
	 * 
	 * @param cause
	 *            The throwable cause
	 */
	public DAIllegalArgumentException(Throwable cause) {
		super(cause);
	}

	/**
	 * Constructor with an error message and a throwable cause.
	 * 
	 * @param message
	 *            The error message
	 * @param cause
	 *            The throwable cause
	 */
	public DAIllegalArgumentException(String message, Throwable cause) {
		super(message, cause);
	}

}
