package exceptions;

/**
 * This defines a checked exception for an index out of bounds.
 * 
 * @author Natacha Gabbamonte 0932340
 * 
 */

public class DAIndexOutOfBoundsException extends Exception {

	private static final long serialVersionUID = 1L;

	/**
	 * Constructor with only an error message.
	 * 
	 * @param message
	 *            The error message
	 */
	public DAIndexOutOfBoundsException(String message) {
		super(message);
	}

	/**
	 * Constructor with only a throwable cause.
	 * 
	 * @param cause
	 *            The throwable cause
	 */
	public DAIndexOutOfBoundsException(Throwable cause) {
		super(cause);
	}

	/**
	 * Constructor with an error message and a throwable cause.
	 * 
	 * @param message
	 *            The error message
	 * @param cause
	 *            The throwable cause
	 */
	public DAIndexOutOfBoundsException(String message, Throwable cause) {
		super(message, cause);
	}

}
